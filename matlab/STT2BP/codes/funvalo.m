F(in+4+N*(6-1))=-d1*x4*x6/v1;                   % 4,6
F(in+5+N*(4-1))=-d1*x5*x4/v1;                   % 5,4
F(in+5+N*(5-1))=-d1*v1-d1*x5^2/v1;              % 5,5
F(in+5+N*(6-1))=-d1*x5*x6/v1;                   % 5,6
F(in+6+N*(4-1))=-d1*x6*x4/v1;                   % 6,4
F(in+6+N*(5-1))=-d1*x6*x5/v1;                   % 6,5
F(in+6+N*(6-1))=-d1*v1 - d1*x6^2/v1;            % 6,6

 in = in+N^2;
