function [F]=funvalo(x,nt,N)
global mu1 c1;
% Evaluates higher order partials of the 
% Orbit Problem in Rauhala's Convention
% nt- No of terms required of higher order partials
% Returns F - vector of dims - N+N^2+...+N^(nt+1)x1
% F(i)= f_i
% F(N+i+N*(j1-1))=f_i,j1
% F(N+N^2+i+N*(j1-1)+N^2*(j2-1))=f_i,j1j2... and so on
%N - Number of states
% N = 6 %%for this problem
% drag parameter assumed to be known

%% NOTE ALL STATES NEED TO BE IN NON DIM UNITS ie RE
%%      ALL VELOCITIES NEED TO BE IN NON DIM UNITS ie RE/TU

%tic

r1 = sqrt(x(1)^2+x(2)^2+x(3)^2); % Magnitude of position
v1 = sqrt(x(4)^2+x(5)^2+x(6)^2); % Magnitude of velocity
x1=x(1); x2=x(2); x3=x(3); x4 = x(4); x5=x(5); x6=x(6);
r15 = r1^5; r13=r1^3; r17=r1^7; r19=r1^9;
v13=v1^3; v15 = v1^5;  
d1= 0.5*c1;                      % drag parameter constant

F(N*(1-N^(nt+1))/(1-N),1)=0;  % sum to nt terms in GP w/ a=N, r=N
F(1,1) = x(4);
F(2,1) = x(5);
F(3,1) = x(6);
F(4,1) = -mu1*x(1)/r13 - d1*v1*x(4);
F(5,1) = -mu1*x(2)/r13 - d1*v1*x(5);
F(6,1) = -mu1*x(3)/r13 - d1*v1*x(6);

